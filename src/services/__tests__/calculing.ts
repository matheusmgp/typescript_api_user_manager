export const sum = (n: number, y: number): number => {
  return n + y;
};

export const subtraction = (n: number, y: number): number => {
  return n - y;
};

export const division = (n: number, y: number): number => {
  return n / y;
};

export const multiplication = (n: number, y: number): number => {
  return n * y;
};
